var actualLanguage = (localStorage.getItem("language")) ? localStorage.getItem("language") : "🇬🇧"
var languageList = ["🇪🇸", "🇩🇪", "🇫🇷", "🇬🇧", "🇨🇳"];
changeLanguage(actualLanguage);


$('.language-item').on("click", (e) => {
    changeLanguage(e.target.innerHTML)
});

function changeLanguage(element) {
    switch (element) {
        case "🇪🇸":
            displayLanguageText(userLanguage.es, "🇪🇸");
            break;
        case "🇩🇪":
            displayLanguageText(userLanguage.de, "🇩🇪");
            break;
        case "🇫🇷":
            displayLanguageText(userLanguage.fr, "🇫🇷");
            break;
        case "🇬🇧":
            displayLanguageText(userLanguage.en, "🇬🇧");
            break;
        case "🇨🇳":
            displayLanguageText(userLanguage.cn, "🇨🇳")
            break;
    }
}

function displayLanguageText(languageObject, language) {
    jQuery.each(languageObject, function (i, val) {
        document.querySelectorAll("[data-" + i + "]").forEach((el) => {
            el.childNodes[0].nodeValue = val;
        })
    })
    localStorage.setItem("language", language)
    actualLanguage = language
    changeLanguageDisplay(language)
}

function changeLanguageDisplay(language) {
    $('#dropdownLanguage')[0].innerHTML = language
    var index = 0;
    for (var i = 0; i < languageList.length; i++) {
        if (languageList[i] != language)
            $('.dropdown-language .language-item')[index++].innerText = languageList[i];
    }
}